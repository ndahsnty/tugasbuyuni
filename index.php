<script>
  $submit = false;
  $(".alert-name").hide();
  $(".alert-email").hide();
  $(".alert-subjek").hide();
  $(".alert-message").hide();
  function sendData(){
    $text_name = $(".name").val();
    $text_email = $(".email").val();
    $text_subjek = $(".subjek").val();
    $text_message = $(".message").val();

    if($text_name == ""){
      $(".alert-name").show();
      $submit = false;

    }else{
      $(".alert-name").hide();
      $submit = true;
    }

    
    if($text_email == ""){
      $(".alert-email").show();
      $submit = false;
    }else{
      $(".alert-email").hide();
      $email = $text_email.indexOf("@");
      if($email < 0){
        $(".alert-email").show();
        $(".alert-email").empty();
        $(".alert-email").append("<strong>Maaf,</strong> email yang anda masukkan salah!");
      }else{
        $(".alert-email").hide();
        $submit = true;
      }
    }

    
    if($text_subjek == ""){
      $(".alert-subjek").show();
      $submit = false;
    }else{
      $(".alert-subjek").hide();
      $submit = true;
    }

    
    if($text_message == ""){
      $(".alert-message").show();
      $submit = false;
    }else{
      $(".alert-message").hide();
      $submit = true;
    }

    if($submit == true){
      swal({
        title: "Terima Kasih!",
        text: "Sudah memberikan saran ataupun kritikan terhadap kami!",
        icon: "success",
        button: "Ok!",
      }).then((value) => {
        location.href = "index.html";
      });
    }


  }
</script>

